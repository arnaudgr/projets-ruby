##{} permet d'insérer le résultat d'une variable ou d'un calcul comme ici

puts "On va compter le nombre d'heure de travail à THP" #affiche le texte
puts "Travail : #{10*5*11}" #affiche le texte + le résultat du clacul
puts "En minute ça fait : #{10*5*11*60}" #affiche le texte + le résultat du clacul

puts "Et en secondes ?" #affiche le texte

puts 10*5*11*60*60 #affiche le résultat d'un calcul

puts "Est-ce que c'est vrai que 3+2 < 5-7 ?" #affiche le texte

puts 3+2<5-7 #affiche le résultat d'un calcul

puts "Ca fait combien 3+2 ? #{3+2}" #affiche le texte + le résultat du clacul
puts "ca fait combien 5-7 ? #{5-7}" #affiche le texte + le résultat du clacul

puts "Ok, c'est faux alors !" #affiche le texte

puts "C'est drôle ça faisons en plus :" #affiche le texte

puts "Est ce que 5 est plus grand que -2 ? #{5>-2}"  #affiche le texte + le résultat booléen
puts "Est ce que c'est vrai que 5 est supérieur ou égal à -2 ? #{5>=-2}" #affiche le texte + le résultat booléen
puts "Est ce que c'est vrai que 5 est inférieur ou égal à -2 ? #{5<=-2}" #affiche le texte + le résultat booléen
